>>wget<<
[wget "option" "URL"]#指定したURLのファイルをダウンロード
"option"
$ -b        #バックグラウンドで実行する
$ -o        #経過のメッセージを指定したファイルに出力する
$ -a        #-oオプションと同じであるが、指定したファイルが既にある場合は上書きではなく追加する
$ -d        #動作内容を詳しく出力する
$ -q        #経過のメッセージを出力しない
$ -v        #経過のメッセージを出力する(デフォルト)
$ -i        #指定したファイルからダウンロードするURLを読み込む
$ -t        #リトライ回数を指定する(デフォルトは20回)
$ -T        #タイムアウトするまでの秒数を指定する(デフォルトは900秒)
$ -w        #リトライの待ち時間を指定する
$ -nc       #ファイルを上書きしない
$ -c        #途中までダウンロードされていたら続きをダウンロードする
$ -N        #ファイルのタイムスタンプをサーバ上のタイムスタンプに変更する
$ --no-use-server-timestamps        #ファイルのタイムスタンプを変更しない
$ --spider        #ファイルをダウンロードせず、URLの存在だけをチェックする
$ --user=         #ダウンロードする際のユーザー名を指定する
$ --password=     #ダウンロードする際のパスワードを入力する
$ --ask-password  #接続毎にパスワードを入力する
$ -p        #ローカル環境で表示するための必要なファイルも含めてダウウンロードする
$ -r        #再帰的にダウンロード
$ -l        #指定した回数分リンクをたどる
$ -k        #ダウンロード後に、ダウンロードファイル内のURLをローカル閲覧用に変換する
$ -K        #ファイルを変換した際のバックアップを.origという拡張子を付けて保存する
$ -nd       #再帰ダウンロード時にディレクトリを作成しない
$ -nH       #再帰ダウンロード時にホスト名のディレクトリを作成しない
$ --max-redirect=       #URLリダイレクトの回数を指定する
$ -R        #指定した拡張子のファイルはダウンロードしない
$ -A        #指定した拡張子のみを取得する
$ -D        #ダウンロードするドメインのリスト
$ --exclude-domains       #ダウンロードしないドメインのリスト
$ -np       #親ディレクトリは対象としない
>><<

























