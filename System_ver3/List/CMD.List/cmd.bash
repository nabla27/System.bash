>>cp<<
[cp "option" "file1" "file2" "directory"]#ファイルを指定したディレクトリにコピー(複数選択可)
[cp "option" "original_file" "cp_file_name"]#ファイルを既存もしくは新しい指定したファイル名にコピー
"option"
$ -i        #上書きする前に確認する
$ -v        #実行内容を表示する
$ -n        #存在するファイルを上書きしない
$ -f        #強制的に上書きする
$ -b        #上書きされるファイルのバックアップを作る

$ -d        #シンボリックリンク自体をコピーする
$ -s        #コピーの代わりにシンボリックリンクを作成する
$ -l        #コピーの代わりにファイルのハードリンクを作成する
$ -L        #コピー元のシンボリックリンクを常にたどる
$ -P        #コピー元にあるシンボリックリンクをたどらない

$ -R        #コピー元にディレクトリを指定し場合、再帰的にコピーする
$ -t        #指定したディレクトリにコピーする
$ -T        #コピー先がディレクトリでも特別扱いしない

$ -a        #サブディレクトリや属性など、可能な限り全て保持しながらコピーする
$ -u        #コピー元のファイルがコピー先のファイルより新しいか新規ファイルの場合のみコピーする
$ -p        #パーミッションと所有者とタイムスタンプを保持する
$ --preserve=       #指定した属性を保持する(/mode/ownership/timestamps/context/links/xattr)
$ --no-preserve     #指定した属性を保持しない
$ --attributes-only       #ファイルのデータをコピーせず、ファイルの属性のみをコピーする
>><<


